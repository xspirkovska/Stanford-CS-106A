public class Variables {

	/*
		Static variable vs Instance variable : 
		Instance variable belongs to an instance of the class, aka object, where as static variable belongs to the class itself. 
		Differences: 
		1. You need to create a instance of the class to be able to use instance variable, but you don't need to create an instance to use static variable
		2. Static variable is shared by all instances of class
	*/

	String instanceVariable = "This is a instance variable";
	static String staticVariable = "This is a global variable";	
}